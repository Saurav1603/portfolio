// Reduced motion variant (respects prefers-reduced-motion)
export const getReducedMotionVariant = (variant) => {
  if (typeof window !== 'undefined' && window.matchMedia('(prefers-reduced-motion: reduce)').matches) {
    return {
      hidden: { opacity: 0 },
      visible: { opacity: 1 },
    };
  }
  return variant;
};
